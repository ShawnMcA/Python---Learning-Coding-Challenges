# Lists - comparable to Arrays. Order is persistant
_list = ['James', 'Mary', 'Steve']

# Tuple - similar to a list, but immutable
_tuple = ('James', 'Mary', 'Steve')

# Dict (Dictionary) - similar to a JS object, key-value pair, hash table - order is not maintained
_dictionary = { 1: 'James', 2: 'Mary', 3: 'Steve' }

# Set - similar to a dict, but can only contain unique keys
_set = { 'James', 'Mary', 'Steve' }


print(type(_list))
print(type(_tuple))
print(type(_dictionary))
print(type(_set))